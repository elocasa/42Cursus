#include "push_swap.h"

void    ft_error(char *msg)
{
    ft_putendl_fd(msg, 1);
    exit (0);
}

void	ft_free(char **str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	while (i >= 0)
		free(str[i--]);
}
